export function getGameById(id: string): Game | undefined {
  return games.find((g) => g.id === id)
}

export function getGamesByFilter(filters: {
  genres?: string[]
  devices?: string[]
  difficulty?: string[]
  platforms?: string[]
}): Game[] {
  return games.filter((game) => {
    if (filters.genres?.length && !filters.genres.some((g) => game.genres.includes(g))) return false
    if (filters.devices?.length && !filters.devices.some((d) => game.devices.includes(d))) return false
    if (filters.difficulty?.length && !filters.difficulty.includes(game.difficulty)) return false
    if (filters.platforms?.length && !filters.platforms.some((p) => game.platforms.includes(p))) return false
    return true
  })
}
